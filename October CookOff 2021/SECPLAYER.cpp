void rsolve() {
  int n;
  cin >> n;
  vector<pii> v(n);
  for(int i = 0; i < n; i++) {
    cin >> v[i].f;
    v[i].s = i;
  }
  sort(all(v));
  mnum base = 2;
  mnum scale = 5;
  mnum inc = 4;
  for(int i = 4; i <= n; i++) {
    base *= scale;
    scale += inc++;
  }
  mnum nn = 2;
  mnum dd = 4;
  mnum ret = 0;
  mnum amt = 0;
  for(int i = n-2; i >= 0; i--) {
    ret += v[i].f * base;
    amt += base;
    base *= nn++;
    base /= dd++;
  }
  cout << ret/amt << "\n";
}
void solve() {
  int t;
  cin >> t;
  while(t--) rsolve();
}
